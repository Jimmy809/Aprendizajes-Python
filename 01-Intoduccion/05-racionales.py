# Operadores Racionales

a = 10
b = 7

# Mayor que >
print('a es mayor que b?', a>b)

# Menor que <
print('a es menor que b?', a<b)

# Igual a ==
print('a es igual que b?',a==b)

# No es igual a !=
print('a es distinto que b?',a!=b)

# Mayor o igual >=
print('a es igual que b?',a>=b)

#Menor o igual<=
print('a es igual que b?',a<=b)