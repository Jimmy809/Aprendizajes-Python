# tipos de operadores aritmeticos
# Operador	      Nombre	        Ejemplo
# +	               Suma	           x + y = 13
# -	               Resta	       x - y = 7
# *	            Multiplicación	   x * y = 30
# /	              División	       x/y = 3.333
# %	               Módulo	       x%y = 1
# **	          Exponente	       x ** y = 1000
# //	           Cociente	       3

x = 10; y = 3
print("Operadores aritméticos")
print("resultado de la suma x+y =", x+y)   #13
print("resultado de la resta x-y =", x-y)   #7
print("resultado de la multiplicacion x*y =", x*y)   #30
print("resultado de la division x/y =", x/y)   #3.3333333333333335
print("resultado del modulo x%y =", x%y)   #1
print("resultado del exponente x**y =", x**y) #1000
print("resultado del cociente x//y =", x//y) #3