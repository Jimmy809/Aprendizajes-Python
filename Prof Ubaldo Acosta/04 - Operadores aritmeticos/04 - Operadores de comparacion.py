# operadores relacionales o de comparacion
# Operador	        Nombre	        Ejemplo
# ==	        Igual	            x == y = False
# !=	        Distinto	        x != y = True
# >	            Mayor	            x > y = False
# &lt	        Menor	            x &lt y = True
# >=	        Mayor o igual	    x >= y = False
# <=	        Menor o igual	    x < y = True

x=2; y=3
print("Operadores Relacionales")
print("x==y =", x==y) # False
print("x!=y =", x!=y) # True
print("x>y  =", x>y)  # False
print("x<y  =", x<y)  # True
print("x>=y =", x>=y) # False
print("x<=y =", x<=y) # True