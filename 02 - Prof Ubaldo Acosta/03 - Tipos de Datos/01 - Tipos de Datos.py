# Datos de tipos numericos
x = 10
print(x)
print(type(x)) # para saber el tipo de dato que contiene - <class 'int'>


# Datos de tipos cadena o string
y = 'Hola Mundo'
print(y)
print(type(y)) # para saber el tipo de dato que contiene - <class 'str'>


# Datos de tipos decimal o float
z = 10.5
print(z)
print(type(z)) # para saber el tipo de dato que contiene - <class 'float'>


# Datos de tipos Buliano o Bool
w = True # debemos respetar las mayusculas True o False
print(w)
print(type(w)) # para saber el tipo de dato que contiene - <class 'bool'>